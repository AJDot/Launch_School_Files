def each_cons(collection, group_size)
  index = 0
  while index < collection.size - (group_size - 1)
    yield(*collection[index, group_size])
    index += 1
  end
end
 # OR
def each_cons(collection, group_size)
  collection.each_with_index do |item, idx|
    (break if idx >= collection.size - (group_size - 1))
    yield(*collection[idx, group_size])
  end
end


hash = {}
each_cons([1, 3, 6, 10], 1) do |value|
  hash[value] = true
end
p hash == { 1 => true, 3 => true, 6 => true, 10 => true }

hash = {}
each_cons([1, 3, 6, 10], 2) do |value1, value2|
  hash[value1] = value2
end
p hash == { 1 => 3, 3 => 6, 6 => 10 }

hash = {}
each_cons([1, 3, 6, 10], 3) do |value1, *values|
  hash[value1] = values
end
p hash == { 1 => [3, 6], 3 => [6, 10] }

